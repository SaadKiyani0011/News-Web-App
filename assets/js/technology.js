        // DOM elements
        const techGrid = document.getElementById('techGrid');
        const techModal = document.getElementById('techModal');
        const modalTitle = document.getElementById('modalTitle');
        const modalCategory = document.getElementById('modalCategory');
        const modalDate = document.getElementById('modalDate');
        const modalAuthor = document.getElementById('modalAuthor');
        const modalImage = document.getElementById('modalImage');
        const modalBody = document.getElementById('modalBody');
        const modalClose = document.getElementById('modalClose');
        const currentDate = document.getElementById('currentDate');

        // Display current date
        function updateDate() {
            const now = new Date();
            const options = { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' };
            currentDate.textContent = now.toLocaleDateString('en-US', options);
        }

        // Generate technology news cards
        function renderTechnologyCards() {
            techGrid.innerHTML = '';
            
            technologyNews.forEach(news => {
                const card = document.createElement('div');
                card.className = 'tech-card';
                card.innerHTML = `
                    <img src="${news.image}" alt="${news.title}" class="tech-image">
                    <div class="tech-content">
                        <span class="tech-category">${news.category}</span>
                        <h3 class="tech-title">${news.title}</h3>
                        <p class="tech-excerpt">${news.excerpt}</p>
                        <div class="tech-meta">
                            <span>${news.date}</span>
                            <span>${news.author}</span>
                        </div>
                    </div>
                `;
                
                card.addEventListener('click', () => openNewsModal(news));
                techGrid.appendChild(card);
            });
        }

        // Open modal with news details
        function openNewsModal(news) {
            modalTitle.textContent = news.title;
            modalCategory.textContent = news.category;
            modalDate.textContent = news.date;
            modalAuthor.textContent = `By ${news.author}`;
            modalImage.src = news.image;
            modalImage.alt = news.title;
            modalBody.innerHTML = news.content;
            
            techModal.style.display = 'block';
            document.body.style.overflow = 'hidden';
        }

        // Close modal
        function closeNewsModal() {
            techModal.style.display = 'none';
            document.body.style.overflow = 'auto';
        }

        // Event listeners
        modalClose.addEventListener('click', closeNewsModal);
        techModal.addEventListener('click', (e) => {
            if (e.target === techModal) closeNewsModal();
        });

        // Initialize
        updateDate();
        renderTechnologyCards();